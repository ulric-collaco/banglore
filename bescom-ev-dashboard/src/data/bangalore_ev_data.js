const WEIGHTS = [0.3, 0.25, 0.2, 0.15, 0.1];

function normalizeFactor(values, value) {
  const min = Math.min(...values);
  const max = Math.max(...values);
  return max === min ? 1 : (value - min) / (max - min);
}

